        <h1>HISTÓRIA</h1>
        <p>A trajetória da Fazenda Água Fria teve início na década de 70, no Sul do Pará. Uma região que não tinha sido explorada para a criação de Nelore, até então. As inúmeras dificuldades enfrentadas na época favoreceram o crescimento da fazenda que se tornou uma difusora da genética bovina no país.</p>
        <p>A história teve início com o visionário João Guimarães e hoje, segue pelas mãos de João Guimarães Filho, que mantém a qualidade do trabalho de seleção que vem sendo feito há quase meio século.</p>
        <p>No início do ano de 1990 foi implantado a Estação de Monta e as primeiras experiências com Inseminação Artificial (IA). No ano de 2000, os animais entraram para o Programa de Melhoramento da Raça Nelore (ANCP) e hoje também participa do Programa de Melhoramento Genético de Zebuínos (PMGZ/ABCZ). Desta forma, os animais são duplamente avaliados, garantindo assim mais confiabilidade nos reprodutores comercializados.</p>
        <p>Já em 2011, foi implantada a FIV (Fecundação In Vitro). Assim, todas as doadoras passaram a ser escolhidas pela qualidade de produção e pela avaliação genética, priorizando suas principais características visuais e raciais.</p>
        <h2>LEILÃO NELORE ÁGUA FRIA</h2>
        <p>Uma nova etapa foi marcada em 2011, com a realização do 1o Leilão de Touros Nelore Água Fria. Realizado sempre no mês de agosto, o evento já é reconhecido nacionalmente, ofertando os melhores reprodutores da safra criados e recriados a pasto, preparados com suplementação e com extrema qualidade genética.</p>
